#include <string> // Required to use text strings!

int main() {
    // 1. Whole Numbers (Python: int)
    int age = 25; 
    
    // 2. Decimals/Fractions (Python: float)
    double price = 19.99; // Always prefer 'double' over 'float' in modern C++
    
    // 3. Single Letters/Symbols (No Python equivalent, Python just uses strings)
    char grade = 'A'; // Must use SINGLE quotes ''
    
    // 4. Text Strings (Python: str)
    std::string name = "Kali User"; // Must use DOUBLE quotes ""

    // 5. True/False (Python: bool)
    bool isRoot = true; // Lowercase 'true' or 'false' (unlike Python's True/False)

    return 0;
}
